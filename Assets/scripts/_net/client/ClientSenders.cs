using UnityEngine;

public class ClientSenders : MonoBehaviour
{
    private static ClientSenders _instance;

    public static ClientSenders Instance
    {
        get => _instance;
        private set
        {
            if (_instance == null)
            {
                _instance = value;
            }
            else if (_instance != value)
            {
                Debug.Log("Duplicate NetworkManager instance in scene!");
                Destroy(value);
            }
        }
    }

    void Awake()
    {
        Instance = this;
    }
}
